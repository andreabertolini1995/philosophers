#include "philo.h"

int	main(int argc, char **argv)
{
	t_dining		*dining;

	if (check_input(argc, argv) == 1)
		return (1);
	dining = initialize_dining_data(argc, argv);
	terminate_threads(dining);
	destroy_mutexes(dining);
	free_data(dining);
	return (0);
}
